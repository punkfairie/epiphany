    <h1>PC History</h1>

    <p>I first bought petz waayy back in the second or third grade at my school's book fair. I was
    hooked immediately! I still use that same copy of Petz 5, and I've tried other versions but Petz
    5 will always be my favorite.</p>

    <p>By the fourth or fifth grade I had discovered the pc. <a
    href="https://web.archive.org/web/20190803214943/http://www.angelfire.com/pa5/petz5world/"
    target="_blank">This</a> is the first site I ever visited. I remember trying to adopt from it,
    not yet understanding the concept of inactive sites. To me, if a site was online it was active,
    and I was highly disapointed when my adoption never came! I wanted my own site too. I wish I
    still had those first versions of my sites to show you, but I can tell you they were pretty bad!
    I was only 9 or 10 at the time, and my HTML skills weren't the best. I learned almost everything
    I know from a site called <a href="http://www.lissaexplains.com/" target="_blank">Lissa Explains
    it All</a>.</p>

    <p>I can't remember if any of my early attempts ever made it online or not. I don't think they
    did, but I had plenty of webs accounts over the years from various abandoned projects. I was
    never into petz enough to stay interested long enough to finish a site, so I had many
    half-finished projects over the years! The first name I can remember having was Somthin' Funny,
    and Epiphany's first layout is named after it. It seemed fitting to give my first successful
    attempt at a site the name that I started with:) Some other names I've had over the years were
    Maddilyn's Munchkins {I used to go by Maddilyn in the pc}, and Starry Night K&C. I'm sure there
    were more but they were all so long ago I can't remember them. There was a pretty long gap
    during most of middle school when I wasn't into petz, and all my stuff from elementary school
    got lost.</p>

    <p>During my sophmore year of high school {2013} I started getting into petz again. That's when
    I started on Maddilyn's Munchkins. I had a Tumblr by that name too, so even though the site
    never made it online I still had somewhat of an online presence in the pc. I got sick of that
    name pretty fast though and changed it too Starry Night K&C, though I can't remember if I ever
    changed the Tumblr. Pretty much all of this happened during christmas break, and once school
    started I got busy and lost interest. I ended up deleting the Tumblr.</p>

    <a href="images/snkc_layout.png" target="_blank">
        <img src="images/snkc_layout.png" height="100" width="100">
    </a>

    <p>Here's what Maddilyn's Munchkins/Starry Night K&C looked like. I had tried to do an inline
    frame layout at first, and couldn't get the frame to get in the right position (I still wasn't
    great at HTML). So I gave up and did regular frames. I also wasn't very creative with fonts or
    backgrounds {the background is from the Petz 5 web fun pack}.</p>

    <p>Now fast forward to the middle of my junior year. I'm in an early college program now, and
    have some more time on my hands as I don't have as many classes. I started getting into petz
    again when I decided to look through my giant folder of favorited petz sites to see if any were
    still alive, and surprisingly, the pc was still alive and fairly active! Once I started going
    through those sites, I got bitten by the "petz bug" so to speak. I opened up my game which had
    sat for a year unused on my computer and once I started playing I knew I had to come back. And
    thus, Epiphany was born!</p>

    <p>I have this wall in my room that's covered with index cards, and they all have interesting
    word definitions on them, I'm a bit of a word nerd:) When I was trying to come up with names I
    looked through all the words on my wall, and epiphany stuck out to me. I was trying to find
    something that was easy to say/spell, and the word epiphany fits, because realizing that I was
    never going to completely leave the pc was something of an epiphany for me.</p>

    <p>Obviously I got wayy better at coding when making Epiphany. When designing it I looked at
    other petz sites for ideas. Then it occured to me to look at the source code of those sites to
    see how they did it, and that's how I learned the HTML to make Epiphany. I did a lot of Google
    searches as well. In the past I'd relied mostly on the one site, which is a bit out of date by
    now, especially design-wise. So thank you to the various active petz sites, I think <a
    href="http://www.freewebs.com/moonshadow87/" target="_blank">Moonflower Petz</a> and <a
    href="http://heart.nu/arie/index.html" target="_blank">Litters Factory</a> especially were the
    two I looked at the most.</p>
